    private void btnAddSuppPurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSuppPurchaseActionPerformed
        if (txtSupplierID.getText().equals("") || txtDrugID.getText().equals("") || boxQuantity.getSelectedIndex() == 0 || txtCostPrice.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Complete Supplier Purchase Information", "Missing Information", JOptionPane.WARNING_MESSAGE);

        } else {
            try {
                String sql = "INSERT INTO supplier_purchases (SUPPLIER_ID, DRUG_ID, PURCHASE_DATE, QUANTITY_BROUGHT, COST_PRICE) VALUES (?, ?, ?, ?, ?)";
                try (PreparedStatement pre = con.prepareStatement(sql)) {
                    pre.setString(1, txtSupplierID.getText());
                    pre.setString(2, txtDrugID.getText());

                    Timestamp purchaseDate = new Timestamp(System.currentTimeMillis());
                    pre.setTimestamp(3, purchaseDate);

                    pre.setString(4, boxQuantity.getSelectedItem().toString());
                    pre.setString(5, txtCostPrice.getText());
                    pre.executeUpdate();

                    JOptionPane.showMessageDialog(null, "Supplier Purchase has been Added Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);

                    clear();
                    refreshSupplierPurchaseTable();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAddSuppPurchaseActionPerformed

    private void btnDeleteSPurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSPurchaseActionPerformed
        if (txtSupplierPurchaseID.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Supplier Purchase ID You want to delete", "Missing Information", 2);
        } else {
            try {
                String sql = "DELETE FROM supplier_purchases WHERE SUPPLIER_PURCHASE_ID=?";
                int confirming = JOptionPane.showConfirmDialog(null, "Are You Sure You Want to Delete this Supplier Purchase?");
                if (confirming == 0) {
                    try (PreparedStatement pre = con.prepareStatement(sql)) {
                        pre.setString(1, txtSupplierPurchaseID.getText());
                        pre.execute();
                        JOptionPane.showMessageDialog(null, "Supplier Purchase has been deleted Successfully", "Success Operation", 1);

                        clear();
                        refreshSupplierPurchaseTable();
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error", 2);
            }
        }
    }//GEN-LAST:event_btnDeleteSPurchaseActionPerformed

    private void tblSupplierPurchasesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSupplierPurchasesMouseClicked
        int row = tblSupplierPurchases.getSelectedRow();
        String supplierPurchaseId = tblSupplierPurchases.getModel().getValueAt(row, 0).toString();
        String sql = "SELECT * FROM supplier_purchases WHERE SUPPLIER_PURCHASE_ID='" + supplierPurchaseId + "' ";

        try {
            pre = con.prepareStatement(sql);
            res = pre.executeQuery();
            if (res.next()) {
                txtSupplierPurchaseID.setText(res.getString("SUPPLIER_PURCHASE_ID"));
                txtSupplierID.setText(res.getString("SUPPLIER_ID"));
                txtDrugID.setText(res.getString("DRUG_ID"));
                boxQuantity.setSelectedItem(res.getString("QUANTITY_BROUGHT"));
                txtCostPrice.setText(res.getString("COST_PRICE"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", 2);
        }
    }//GEN-LAST:event_tblSupplierPurchasesMouseClicked

